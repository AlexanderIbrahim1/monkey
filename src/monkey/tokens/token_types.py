"""
This module contains constants representing all the token types recognized by the
Monkey programming language.
"""

# special token types
ILLEGAL = "ILLEGAL"
EOF = "EOF"

# identifiers
IDENTIFIER = "IDENTIFIER"

# literals
INT = "INT"

# operators
ASSIGN = "="
PLUS = "+"
MINUS = "-"
BANG = "!"
ASTERISK = "*"
SLASH = "/"
LT = "<"
GT = ">"

# punctuation
COMMA = ","
SEMICOLON = ";"

LPAREN = "("
RPAREN = ")"
LBRACE = "{"
RBRACE = "}"

# reserved keywords
FUNCTION = "FUNCTION"
LET = "LET"
